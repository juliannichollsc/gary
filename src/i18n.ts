// GARY 🐾 — lightweight i18n. 4 languages (English default). The OS language picks the initial locale;
// if it isn't one of the four → English. The word "GARY" is the product name and is NEVER translated.
// Usage in a component:  const t = useT();  … t("nav.chat")
import { useSyncExternalStore } from "react";

export type Lang = "en" | "es" | "pt" | "de" | "zh";
export const LANGS: { id: Lang; label: string }[] = [
  { id: "en", label: "English" },
  { id: "es", label: "Español" },
  { id: "pt", label: "Português" },
  { id: "de", label: "Deutsch" },
  { id: "zh", label: "中文" },
];

const LS_KEY = "gary-lang";

function detect(): Lang {
  const saved = localStorage.getItem(LS_KEY) as Lang | null;
  if (saved && LANGS.some((l) => l.id === saved)) return saved;
  const sys = (navigator.language || "en").slice(0, 2).toLowerCase();
  return (["en", "es", "pt", "de", "zh"] as const).includes(sys as Lang) ? (sys as Lang) : "en";
}

let current: Lang = detect();
const listeners = new Set<() => void>();

export function getLang(): Lang { return current; }
export function setLang(l: Lang): void {
  current = l;
  localStorage.setItem(LS_KEY, l);
  document.documentElement.setAttribute("lang", l);
  listeners.forEach((f) => f());
}
document.documentElement.setAttribute("lang", current);

// React binding: re-render subscribers when the language changes.
function subscribe(cb: () => void) { listeners.add(cb); return () => { listeners.delete(cb); }; }
export function useLang(): Lang { return useSyncExternalStore(subscribe, getLang); }

